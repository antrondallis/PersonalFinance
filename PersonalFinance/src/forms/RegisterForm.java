    public boolean formValid(){
        boolean valid = true;
        if (txtFirstName.getText().equals("")){
            txtFirstName.setBorder(new LineBorder(ColorScheme.errorColor, 2));
            valid = false;
        }
        if (txtLastName.getText().equals("")){
            txtLastName.setBorder(new LineBorder(ColorScheme.errorColor, 2));
            valid = false;
        }
        if (txtEmail.getText().equals("")){
            txtEmail.setBorder(new LineBorder(ColorScheme.errorColor, 2));
            valid = false;
        }
        if (txtUserName.getText().equals("")){
            txtUserName.setBorder(new LineBorder(ColorScheme.errorColor, 2));
            valid = false;
        }
        if (txtPassword.getText().equals("")){
            txtPassword.setBorder(new LineBorder(ColorScheme.errorColor, 2));
            valid = false;
        }
        if (txtRetypePwd.getText().equals("")){
            txtRetypePwd.setBorder(new LineBorder(ColorScheme.errorColor, 2));
            valid = false;
        }
            
        
        return valid;
    }
    
