    private void lblLoginAcctMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblLoginAcctMouseClicked
        //if there is any input on the form prompt the user for confimation
        boolean confirmed = true;
        if(!txtFirstName.getText().equals("") || !txtLastName.getText().equals("") ||!txtEmail.getText().equals("") ||
                !txtUserName.getText().equals("") || !txtPassword.getText().equals("") || !txtRetypePwd.getText().equals("")){
            int confirmExit = JOptionPane.showOptionDialog(null, "All Choices will be lost.\n"
                + "Are you sure you want to return to the Login form?", "Return to Login",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, null, null);
            
            confirmed = confirmExit == JOptionPane.YES_OPTION;
        }
        
        if(confirmed){
            this.setVisible(false);
            new LoginForm().setVisible(true);
        }
    }//GEN-LAST:event_lblLoginAcctMouseClicked

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        formDirty = true;
        DataResult result = new DataResult();
        if(!formValid()){
            JOptionPane.showOptionDialog(null, "Please fix the highlighted error", "Error",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
        } else if(!passwordMatch()){
            JOptionPane.showOptionDialog(null, "Password Mismatch", "Error",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
        } else if(!validEmail()){
            JOptionPane.showOptionDialog(null, "Invalid Email Format", "Error",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
        } else if (userNameExist()){
            JOptionPane.showOptionDialog(null, "Username already exists", "Error",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, null, null);
        }
        else{
            UserModel model = new UserModel(txtFirstName.getText().trim(), txtLastName.getText().trim(), 
                    txtEmail.getText().trim(), txtUserName.getText().trim(), txtPassword.getText().trim());
            result = _userModelRepository.RegisterUser(model);
            
            if(result.StatusCode == DataResultStatus.CREATED_STATUS){
                JOptionPane.showOptionDialog(null, "Registration Successful. New account created!!", "Success",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
                this.setVisible(false);
                new LoginForm().setVisible(true);
                
            } else{
                JOptionPane.showOptionDialog(null, "Registration failure: " + result.Message, "Failure",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE, null, null, null);
            }
            
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

